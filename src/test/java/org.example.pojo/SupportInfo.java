package org.example.pojo;

public class SupportInfo {
    private String url;
    private String text;

    public String getUrl() {
        return url;
    }

    public SupportInfo setUrl(String url) {
        this.url = url;
        return this;
    }

    public String getText() {
        return text;
    }

    public SupportInfo setText(String text) {
        this.text = text;
        return this;
    }
}
